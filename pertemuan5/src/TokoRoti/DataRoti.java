    public void showTable() {
        try {
            String sql = "SELECT * FROM tokoroti ORDER BY id_kue";
            ResultSet rs = stmt.executeQuery(sql);

            DefaultTableModel model = new DefaultTableModel(
                    new String[]{"idkue", "nama kue", "harga", "stock"}, 0
            );

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("id_kue"),
                    rs.getString("nama_kue"),
                    rs.getString("harga"),
                    rs.getString("stock")
                });
            }

            tabeldataroti.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tampil data: " + e.getMessage());
        }
    }

