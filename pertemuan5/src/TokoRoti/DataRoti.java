    private void Button_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_tambahActionPerformed
        if (textidkue.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "isi semua data !");
        } else if (textnamakue.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "isi semua data !");
        } else if (textharga.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "isi semua data !");
        } else if (textstock.getText().equals("")) {
            JOptionPane.showMessageDialog(this, " isi semua data !");
        } else {
            try {
                String sql = "INSERT INTO tokoroti (id_kue, nama_kue, harga, stock) VALUES (?,?,?,?)";
                var ps = conn.prepareStatement(sql);
                ps.setString(1, textidkue.getText());
                ps.setString(2, textnamakue.getText());
                ps.setInt(3, Integer.parseInt(textharga.getText()));
                ps.setInt(4, Integer.parseInt(textstock.getText()));
                ps.executeUpdate();
                JOptionPane.showMessageDialog(this, "data berhasil disimpan !");
                showTable();
                bersih();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_Button_tambahActionPerformed

