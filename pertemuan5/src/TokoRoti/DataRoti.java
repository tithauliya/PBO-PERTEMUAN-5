    private void Button_perbaruiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_perbaruiActionPerformed
       if (textidkue.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (textnamakue.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (textharga.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else if (textstock.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Isi Semua Data!");
        } else {
        try {
                String sql = "UPDATE tokoroti SET id_kue=?, nama_kue=?, harga=?, stock=? "
                        + "WHERE id_kue=? AND nama_kue=? AND harga=? AND stock=?";

                var ps = conn.prepareStatement(sql);

                ps.setString(1, textidkue.getText());
                ps.setString(2, textnamakue.getText());
                ps.setInt(3, Integer.parseInt(textharga.getText()));
                ps.setInt(4, Integer.parseInt(textstock.getText()));
                
                ps.setString(5, idkueLama);
                ps.setString(6, namakueLama);
                ps.setInt(7, Integer.parseInt(hargaLama));
                ps.setInt(8, Integer.parseInt(stockLama));
           

                int rows = ps.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!");
                    showTable();
                    bersih();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_Button_perbaruiActionPerformed

